
/*Aqui creamos una clase con las diferentes funciones que queremos que realice nuestra calculadora */
class Calculadora {
    sumar(num1, num2){
        return num1 + num2
    }

    restar(num1, num2){
        return num1 - num2
    }

    dividir(num1, num2){
        return num1 / num2
    }

    multiplicar(num1, num2){
        return num1 * num2
    }
}




